        public static string GetMimeTypeFromExtension(string extension) {
            var result = string.Empty;
            if (!extension.StartsWith("."))
                extension = "." + extension;

            var key = Registry.ClassesRoot.OpenSubKey(extension, false);
            if (key != null) {
                var value = key.GetValue("Content Type");
                if (value != null) {
                    result = value.ToString();
                }
            }
            if (string.IsNullOrEmpty(result) && Mappings.ContainsKey(extension)) {
                result = Mappings[extension];
            }
            return result;
        }
        public static string GetDefaultExtension(string mimeType) {
            var result = string.Empty;
            var key = Registry.ClassesRoot.OpenSubKey(@"MIME\Database\Content Type\" + mimeType, false);
            if (key != null) {
                var value = key.GetValue("Extension");
                if (value != null) {
                    result = value.ToString();
                }
            }
            if (string.IsNullOrEmpty(result)) {
                var map = Mappings.FirstOrDefault(tmp => tmp.Value == mimeType);
                if (!string.IsNullOrEmpty(map.Key)) {
                    result = map.Key;
                }
            }
            return result;
        }

